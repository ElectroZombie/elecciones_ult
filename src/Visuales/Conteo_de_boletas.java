    private void Actualizar_tabla(){
        
        borrar_vector();
        
        
        
         DefaultTableModel d = new DefaultTableModel(){
             @Override
             public boolean isCellEditable(int row, int column) {
                return column == 3 || column == 4;         
             }
         };
         
         int cont = 0;
         
         for(int i = 0; i < E.getNombres_extra().size();i++){
             if(E.getNombres_extra().elementAt(i).getN2().getEntero() == 1){
                 cont++;
             }
         }
         
         Object[][] OBJ = new Object[E.getNombres().size() + cont][5];
    
        for(int i = 0; i < E.getNombres().size(); i++){
            OBJ[i][0] = E.getNombres().elementAt(i).getNombre();
            OBJ[i][1] = E.getNombres().elementAt(i).getCarrera();
            OBJ[i][2] = E.getNombres().elementAt(i).getAño();
            vector.add(new JRadioButton("", false));
            OBJ[i][3] = vector.lastElement();
            vector.add(new JRadioButton("", false));
            OBJ[i][4] = vector.lastElement();
        }
        int Cont = 0;
        for(int i = 0; i < E.getNombres_extra().size(); i++){
            if(E.getNombres_extra().elementAt(i).getN2().getEntero() == 1){
            int j = Cont + E.getNombres().size();
            Cont++;
            OBJ[j][0] = E.getNombres_extra().elementAt(i).getN1().getNombre();
            OBJ[j][1] = E.getNombres_extra().elementAt(i).getN1().getCarrera();
            OBJ[j][2] = E.getNombres_extra().elementAt(i).getN1().getAño();
            vector.add(new JRadioButton("", false));
            OBJ[j][3] = vector.lastElement();
            vector.add(new JRadioButton("", false));
            OBJ[j][4] = vector.lastElement();
            }
        }
        
        
       Tabla = new JTable(d);
       d.setDataVector(OBJ, new Object[]{
           "Nombre","Carrera","Año","Voto por miembro","Voto por presidente"
       });
       
       Tabla.setFont(new Font("arial", Font.BOLD, 14));
       Tabla.setRowHeight(30);
       Tabla.setShowGrid(true);
       
       Tabla.getColumn("Voto por miembro").setCellRenderer(
        new RadioButtonRenderer());
       Tabla.getColumn("Voto por miembro").setCellEditor(
        new RadioButtonEditor(new JCheckBox()));
       Tabla.getColumn("Voto por presidente").setCellRenderer(
        new RadioButtonRenderer());
       Tabla.getColumn("Voto por presidente").setCellEditor(
        new RadioButtonEditor(new JCheckBox()));
       
       jScrollPane1.setViewportView(Tabla);
       
    }
    
    
    
    
