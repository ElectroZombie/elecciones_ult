    private void Agregar_boletaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Agregar_boletaMouseClicked
        if (E.getVotos() > E.getMatricula() - 1) {
            JOptionPane.showMessageDialog(null, "Los votos exceden a la matrícula", "Error", JOptionPane.ERROR_MESSAGE);
        }
        E.setVotos(E.getVotos()+1);
        Boleta B = crear_boleta();
        Operaciones_de_boletas.guardar_boleta(B, E);
        Operaciones_de_boletas.limpiar_candidatos_extras(E);
       Sonido sound= new Sonido();
       Votos.setText(E.getVotos() + "");
        sound.sonido("audio");
        Actualizar_tabla();
    }//GEN-LAST:event_Agregar_boletaMouseClicked

