    private Boleta crear_boleta() {
        Vector<Tupla<Candidato, Tupla<Boolean, Boolean>>> V = new Vector<>();
        V.add(new Tupla(null, new Tupla(null, null)));

        for (int i = 0; i < E.getNombres().size(); i++) {
            int j = i + 1;
            V.add(new Tupla(E.getNombres().elementAt(i), new Tupla(vector.elementAt(j * 2 - 1).isSelected(), vector.elementAt(j * 2).isSelected())));

        }
        int C = 0;
        for (int i = 0; i < E.getNombres_extra().size(); i++) {
            if(E.getNombres_extra().elementAt(i).getN2().getEntero()==1){
            int j = E.getNombres().size() + 1 + C;
            C++;
            V.add(new Tupla(E.getNombres_extra().elementAt(i).getN1(), new Tupla(vector.elementAt(j * 2 - 1).isSelected(), vector.elementAt(j * 2).isSelected())));
            }
        }
        switch (E.getTipo_de_eleccion()) {
            case "Residencia" -> {
                return new Boleta(V, 5);
            }
            case "Facultad" -> {
                return new Boleta(V, 7);
            }
            case "Universidad" -> {
                return new Boleta(V, 9);
            }
            default -> {
            return null;
            }
        }
    }

